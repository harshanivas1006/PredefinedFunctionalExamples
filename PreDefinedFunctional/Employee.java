package PreDefinedFunctional;

public class Employee {

	private int id;
	private int salary;
	private String gender;
	public int getId() {
		return id;
	}
	public void setId(int i) {
		this.id = i;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int i) {
		this.salary = i;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	
}
